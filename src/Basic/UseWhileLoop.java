package Basic;

public class UseWhileLoop {
    public static void main(String[] args) {
        int numOfContest=0;
        while(numOfContest<5) {
           // System.out.println("number of contestants are " + numOfContest);
            numOfContest++;
        }
        System.out.println("number of contestants are " + numOfContest);
    }
}
